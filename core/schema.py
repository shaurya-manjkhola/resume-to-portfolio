from pydantic import BaseModel, Field
from typing import List, Optional


class ExperienceItem(BaseModel):
    """
    Schema for a single professional work experience entry.
    """
    company: str = Field(..., description="The name of the company or organization")
    position: str = Field(..., description="The student's official job title or role")
    location: str = Field(..., description="City and State, or 'Remote'")
    duration: str = Field(..., description="The time frame of employment, e.g., 'June 2024 - Present'")
    highlights: List[str] = Field(..., description="Enhanced narrative bullet points")


class ProjectItem(BaseModel):
    """
    Schema for a single technical project entry.
    """
    title: str = Field(..., description="The name of the project")
    description: str = Field(
        ...,
        description="An engaging, 2-3 sentence narrative detailing the problem, solution, and impact"
    )

    tech_stack: List[str] = Field(
        ...,
        description="Programming languages and frameworks used"
    )

    github_url: Optional[str] = Field(
        None,
        description="Project repository URL if available"
    )


class EducationItem(BaseModel):
    """
    Schema for a single education entry.
    """
    institution: str = Field(..., description="The name of the school, college, or university")
    degree: str = Field(..., description="The degree or program, e.g., 'B.S. in Computer Science'")
    duration: str = Field(..., description="The time frame of study, e.g., 'Aug 2022 - May 2026'")
    details: Optional[str] = Field(
        None,
        description="Optional extra detail such as GPA, honors, or relevant coursework, only if present in the resume"
    )


class CertificationItem(BaseModel):
    """
    Schema for a single certification or credential entry.
    """
    name: str = Field(..., description="The name of the certification or credential")
    issuer: str = Field(..., description="The organization that issued it, e.g. 'AWS', 'Coursera'")
    date: Optional[str] = Field(None, description="Date earned, only if stated in the resume")


class VolunteerItem(BaseModel):
    """
    Schema for a single volunteer or community work entry.
    """
    organization: str = Field(..., description="The name of the organization")
    role: str = Field(..., description="The role or contribution")
    duration: str = Field(..., description="The time frame, e.g. 'Jan 2024 - Present'")
    description: Optional[str] = Field(
        None,
        description="What was done, only if stated in the resume"
    )


class PortfolioProfile(BaseModel):
    """
    The master schema containing the fully parsed and enhanced portfolio dataset.
    """
    name: str = Field(..., description="The student's full name")
    headline: str = Field(..., description="A punchy, modern professional subtitle generated by the AI")
    email: str = Field(..., description="The extracted email address")
    github: Optional[str] = Field(None, description="The extracted GitHub profile URL, if present")
    linkedin: Optional[str] = Field(None, description="The extracted LinkedIn profile URL, if present")
    bio: str = Field(
        ...,
        description="A compelling, professional 3-4 sentence background narrative built by the AI"
    )

    experience: List[ExperienceItem] = Field(
        ...,
        description="Professional work experience entries"
    )

    projects: List[ProjectItem] = Field(
        ...,
        description="Technical project entries"
    )

    education: List[EducationItem] = Field(
        default_factory=list,
        description="Education entries such as degrees, schools, and graduation dates"
    )

    certifications: List[CertificationItem] = Field(
        default_factory=list,
        description="Certifications or credentials, only include if explicitly present in the resume"
    )

    volunteer_work: List[VolunteerItem] = Field(
        default_factory=list,
        description="Volunteer or community work entries, only include if explicitly present in the resume"
    )

    skills: List[str] = Field(
        ...,
        description="List of skills"
    )


